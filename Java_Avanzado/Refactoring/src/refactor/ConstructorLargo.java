package refactor;

public class ConstructorLargo {
    String marca;
    String modelo;
    String motor;
    int puertas;

    public ConstructorLargo(String marca, String modelo, String motor, int puertas) {
            this.marca = marca;
            this.modelo = modelo;
            this.motor = motor;
            this.puertas = puertas;
    }

}
